package com.iu.customer;

public class BookStyleDTO {
	private String id;
	private String bs_kind;
	private int step;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getBs_kind() {
		return bs_kind;
	}
	public void setBs_kind(String bs_kind) {
		this.bs_kind = bs_kind;
	}
	public int getStep() {
		return step;
	}
	public void setStep(int step) {
		this.step = step;
	}
}
